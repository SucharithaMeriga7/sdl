WITH FILTERED_ORDER AS (
    SELECT
        ORDERID,
        CUSTID,
        ITEMNAME,
        CAST(PRICEPERUNIT AS NUMBER(18,2)) AS PRICEPERUNIT,
        CAST(QTY AS NUMBER(18,0)) AS QTY,
        CAST(ORDER_DATE AS DATE) AS ORDER_DATE,
        CAST(STARTDATE AS DATE) AS STARTDATE,
        CAST(ENDDATE AS DATE) AS ENDDATE,
        CAST(ISACTIVE AS STRING) AS ISACTIVE
    FROM {{ ref('stg_order') }}
    WHERE ORDERID IS NOT NULL
      AND CUSTID IS NOT NULL
      AND ITEMNAME IS NOT NULL
      AND PRICEPERUNIT IS NOT NULL
      AND QTY IS NOT NULL
      AND ORDER_DATE IS NOT NULL
),

DEDUPED_ORDER AS (
    SELECT
        ORDERID,
        CUSTID,
        ITEMNAME,
        PRICEPERUNIT,
        QTY,
        ORDER_DATE,
        STARTDATE,
        ENDDATE,
        ISACTIVE,
        ROW_NUMBER() OVER (
            PARTITION BY ORDERID
            ORDER BY ORDER_DATE DESC
        ) AS RN
    FROM FILTERED_ORDER
    QUALIFY RN = 1
)

SELECT
    ORDERID,
    CUSTID,
    ITEMNAME,
    PRICEPERUNIT,
    QTY,
    ORDER_DATE,
    STARTDATE,
    ENDDATE,
    ISACTIVE
FROM DEDUPED_ORDER